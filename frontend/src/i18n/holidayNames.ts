/**
 * Returns the localized holiday name if available, otherwise returns the original English name.
 * Matches on the English `name` field from the Nager.Date API.
 */
export function getLocalizedHolidayName(englishName: string): string {
  const lang = i18n.language;
  if (lang === 'en') return englishName;
  return holidayNames[lang]?.[englishName] || englishName;
}
